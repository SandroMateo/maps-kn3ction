MapMaker.prototype.companyLocations = function(company) {
  var image = "../../public/images/goldblum.png"
  if(company.location !== undefined) {
    var latLng = new google.maps.LatLng(company.location.lat, company.location.lng);
    var marker = new google.maps.Marker({
      position: latLng,
      map: this.map,
      animation: google.maps.Animation.DROP,
      icon: image,
      title: company.name + ": " + company.tagline
    });
    marker.setMap(this.map);
  }
}

